/**
 * Writes a unique build id used by the running app and by /version.json on the server.
 * Run before production builds so deploys can be detected by open browser tabs.
 */
import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const buildId = new Date().toISOString().replace(/[-:TZ.]/g, '').slice(0, 14);
const builtAt = new Date().toISOString();

const versionPayload = JSON.stringify({ buildId, builtAt }, null, 2);

writeFileSync(join(root, 'public', 'version.json'), versionPayload, 'utf8');

const tsContent = `/** Generated by scripts/write-build-version.mjs — do not edit manually. */
export const APP_BUILD_ID = '${buildId}';
export const APP_BUILT_AT = '${builtAt}';
`;

const buildVersionPath = join(root, 'src', 'build-version.ts');
writeFileSync(buildVersionPath, tsContent, 'utf8');

console.log(`Build version: ${buildId}`);
